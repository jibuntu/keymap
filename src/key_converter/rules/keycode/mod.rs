use std::collections::HashMap;

pub struct Keycode {
    keycode_map: HashMap<String, u16>,
}

impl Keycode {
    pub fn new() -> Keycode {
        Keycode {
            keycode_map: get_keycode_map(),
        }
    }

    pub fn from_keyword(&self, arg_keyword: &str) -> Option<u16> {
        let keyword = arg_keyword.to_uppercase();
        if let Some(keycode) = self.keycode_map.get(&keyword) {
            return Some(*keycode);
        }
        None
    }
}


